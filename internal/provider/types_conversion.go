package provider

import "github.com/hashicorp/terraform-plugin-framework/types"

func fromBoolPtr(v *bool) types.Bool {
	if v == nil {
		return types.Bool{Null: true}
	}
	return types.Bool{Value: *v}
}

func toBoolPtr(v types.Bool) *bool {
	if v.Null || v.Unknown {
		return nil
	}
	return &v.Value
}
